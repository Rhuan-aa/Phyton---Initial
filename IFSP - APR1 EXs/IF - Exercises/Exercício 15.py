n=float(input("Qual foi a nota dada?"))

if 100>=n>=86:
        print(f"O nota é igual ao conceito A")
elif 85>=n>=61:
        print(f"O nota é igual ao conceito B")
elif 60>=n>=36:
        print(f"O nota é igual ao conceito C")
elif 35>=n>=1:
        print(f"O nota é igual ao conceito D")
else:
        print(f"O nota é igual ao conceito E")
